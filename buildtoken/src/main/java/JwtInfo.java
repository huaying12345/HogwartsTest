



public class JwtInfo {
    private String id;
    private String accountUuid;
    private String name;
    private String dingAccountId;
    private String moziUserCode;
    //权限、角色等

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAccountUuid() {
        return accountUuid;
    }

    public void setAccountUuid(String accountUuid) {
        this.accountUuid = accountUuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDingAccountId() {
        return dingAccountId;
    }

    public void setDingAccountId(String dingAccountId) {
        this.dingAccountId = dingAccountId;
    }

    public String getMoziUserCode() {
        return moziUserCode;
    }

    public void setMoziUserCode(String moziUserCode) {
        this.moziUserCode = moziUserCode;
    }


}
